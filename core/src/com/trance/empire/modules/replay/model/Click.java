package com.trance.empire.modules.replay.model;

public class Click {
	
	/**
	 * 倒计时
	 */
	private int t;
	
	private int x;
	
	private int y;

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
