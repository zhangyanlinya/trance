package com.trance.empire.modules.replay.model;

public class Click {
	
	/**
	 * 相对于开始时间+
	 */
	private int t;
	
	private float x;
	
	private float y;

	public int getT() {
		return t;
	}

	public void setT(int t) {
		this.t = t;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
}
